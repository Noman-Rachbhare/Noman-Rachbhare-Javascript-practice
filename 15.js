// break

for(let i=0;i<10;i++)
{
    if(i==6)
    {
        break;
    }
    console.log(i);
}


// Continue
for(let j=0;j<10;j++)
{
    if(j==6)
    {
        continue;
    }
    console.log(j);
    // console.log("Hi");
    
    
}
